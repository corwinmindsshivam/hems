package com.corwinminds.hesmb.Model;

public class DashboardMenu {
    private int dashboardMenu_id;
    private String dashboardMenu_name;
    private String dashboardMenu_Image_Url;

    public int getDashboardMenu_id() {
        return dashboardMenu_id;
    }

    public void setDashboardMenu_id(int dashboardMenu_id) {
        this.dashboardMenu_id = dashboardMenu_id;
    }

    public String getDashboardMenu_name() {
        return dashboardMenu_name;
    }

    public void setDashboardMenu_name(String dashboardMenu_name) {
        this.dashboardMenu_name = dashboardMenu_name;
    }

    public String getDashboardMenu_Image_Url() {
        return dashboardMenu_Image_Url;
    }

    public void setDashboardMenu_Image_Url(String dashboardMenu_Image_Url) {
        this.dashboardMenu_Image_Url = dashboardMenu_Image_Url;
    }
}
